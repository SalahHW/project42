#include "libft.h"

int	ft_strncmp(const char *s1, const char *s2, size_t n)
{
	size_t	i;

	i = 0;
	while ((i < n - 1) && (s1[i] == s2[i]) && (s1[i]) && (s2[i]))
	{	
		if (n == 0 || s1[i] == '\0' || s2[i] =='\0')
			break;
		i++;
	}
	if ((s1[i] - s2[i]) > 0)
		return (1);
	if ((s1[i] - s2[i]) < 0)
		return (-1);
	return (0);
}
